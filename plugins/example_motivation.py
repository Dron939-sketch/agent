"""Пример пользовательского плагина: возвращает мотивирующую цитату."""

from __future__ import annotations

import random

from app.services.tools import tool

QUOTES = [
    "Сделай шаг сегодня — завтра поблагодаришь себя.",
    "Маленькие действия создают большие результаты.",
    "Лучший момент начать — был год назад. Второй лучший — сейчас.",
    "Сначала плохо, потом хорошо, потом мастерство.",
]


@tool(description="Возвращает случайную мотивирующую цитату.")
async def motivation() -> str:
    """Случайная цитата для подбадривания."""
    return random.choice(QUOTES)
